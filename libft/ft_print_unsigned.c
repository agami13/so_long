#include "libft.h"

int	ft_putunbr(unsigned int n)
{
	int		print_length;
	char	*num;

	print_length = 0;
	if (n == 0)
		print_length += write(1, "0", 1);
	else
	{
		num = ft_itoa(n);
		print_length += ft_putstr(num);
		free(num);
	}
	return (print_length);
}
